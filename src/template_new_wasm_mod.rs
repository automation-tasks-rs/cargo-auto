//! template for new_wasm
//!
//! An automation task copy the content of the template_new_wasm folder into this strings.
//! When installing a crate from crates.io, only the code is transferred. No additional files.

use crate::{GREEN, RED, RESET, YELLOW};

pub fn new_wasm(arg_2: Option<String>, arg_3: Option<String>, arg_4: Option<String>, arg_5: Option<String>) {
    if arg_2.is_none() {
        println!("{RED}Error: Project name argument is missing: `cargo auto new_wasm project_name github_owner web_server server_username`{RESET}");
        return;
    }
    if arg_3.is_none() {
        println!("{RED}Error: Github owner argument is missing: `cargo auto new_wasm project_name github_owner web_server server_username`{RESET}");
        return;
    }
    if arg_4.is_none() {
        println!("{RED}Error: Web server argument is missing: `cargo auto new_wasm project_name github_owner web_server server_username`{RESET}");
        return;
    }
    if arg_5.is_none() {
        println!("{RED}Error: Server username argument is missing: `cargo auto new_wasm project_name github_owner web_server server_username`{RESET}");
        return;
    }
    let project_name = arg_2.unwrap();
    let github_owner = arg_3.unwrap();
    let web_server_domain = arg_4.unwrap();
    let server_username = arg_5.unwrap();

    copy_to_files(&project_name, &github_owner, &web_server_domain, &server_username);
    println!("");
    println!("    {YELLOW}The command `crate auto new_wasm` generated the directory `{project_name}`{RESET}");
    println!("    {YELLOW}You can open this new Rust project `{project_name}` in a new Rust editor.{RESET}",);
    println!("    {YELLOW}For example VSCode:{RESET}");
    println!("{GREEN}code {project_name}{RESET}");
    println!("    {YELLOW}Then build with:{RESET}");
    println!("{GREEN}cargo auto build{RESET}");
    println!("    {YELLOW}and follow the detailed instructions.{RESET}");
}

pub fn copy_to_files(project_name: &str, github_owner: &str, web_server_domain: &str, server_username: &str) {
    let folder_path = std::path::Path::new(project_name);
    std::fs::create_dir_all(folder_path).unwrap();
    for file_item in get_vec_file() {
        // rename/replace the project_name
        let file_name = file_item.file_name.replace("cargo_auto_template_new_wasm", project_name);
        let file_content = file_item.file_content.replace("cargo_auto_template_new_wasm", project_name);
        let file_content = file_content.replace("/github_owner/", &format!("/{github_owner}/"));
        let file_content = file_content.replace("web_server_domain", web_server_domain);
        let file_content = file_content.replace("server_username", server_username);

        // create directory if needed
        std::fs::create_dir_all(folder_path.join(&file_name).parent().unwrap()).unwrap();
        std::fs::write(folder_path.join(&file_name), file_content.as_bytes()).unwrap();
    }
}

